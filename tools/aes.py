# 32位循环左移8位一次
def rot_word(word) -> int:
    return ((word << 8) & 0xFFFFFFFF) | (word >> 24)


# 使用 S-Box 替换一个字
def sub_word(word) -> int:
    return (sbox[(word >> 24) & 0xFF] << 24 |
            sbox[(word >> 16) & 0xFF] << 16 |
            sbox[(word >> 8) & 0xFF] << 8 |
            sbox[word & 0xFF])


# 有限域 GF(2^8) 上的乘法
def gf_mul(a, b) -> int:
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        hi_bit_set = a & 0x80
        a <<= 1
        if hi_bit_set:
            a ^= 0x1B  # 不可约多项式
        b >>= 1
    return p & 0xFF


class Aes:
    def __init__(self):
        """
        初始化 AES 类
        """
        self.Nb = 4  # 状态矩阵的列数
        self.state = np.zeros((4, self.Nb), dtype=np.uint8)  # 初始化状态矩阵，4 行 Nb 列，数据类型为无符号 8 位整数

    def set_state(self, data: np.array):
        """
        将输入数据设置到状态矩阵中

        :param data: 输入数据，长度为 4 * Nb的数组
        """
        for i in range(self.Nb):
            for j in range(4):
                self.state[j][i] = data[i * self.Nb + j]

    def get_state(self) -> np.array:
        """
        获取状态矩阵中的数据

        :return: 状态矩阵中的数据，返回一个长度为 4 * Nb的数组
        """
        return self.state.flatten(order='F')  # order='F' 列优先的顺序进行展平

    def print_state(self):
        """
        打印十六进制状态矩阵
        """
        hex_state = [[f'{byte:02x}' for byte in row] for row in self.state]
        for row in hex_state:
            print(' '.join(row))

    def add_roundkey(self, w):
        """
        将轮密钥添加到状态矩阵中

        :param w: 轮密钥，长度为 Nb 的列表或数组，每个元素为 32 位整数
        """
        for i in range(self.Nb):
            self.state[0][i] ^= (w[i] >> 24) & 0xFF  # 第 1 行
            self.state[1][i] ^= (w[i] >> 16) & 0xFF  # 第 2 行
            self.state[2][i] ^= (w[i] >> 8) & 0xFF  # 第 3 行
            self.state[3][i] ^= w[i] & 0xFF  # 第 4 行

    def sub_state(self):
        """
        使用 S-Box 替换状态矩阵中的每个字节
        """
        for i in range(4):
            for j in range(self.Nb):
                self.state[i][j] = sbox[self.state[i][j]]  # 使用 S-Box 替换当前字节

    def shift_rows(self):
        """
        对状态矩阵的每一行进行循环移位
        """
        for row in range(1, 4):
            for _ in range(row):
                self.state[row] = np.roll(self.state[row], -1)  # 第 row 行左移 row 次

    def mix_columns(self):
        """
        混合列操作，用于扩散状态矩阵中的数据
        """
        for col in range(self.Nb):
            a = self.state[0][col]
            b = self.state[1][col]
            c = self.state[2][col]
            d = self.state[3][col]

            self.state[0][col] = gf_mul(0x02, a) ^ gf_mul(0x03, b) ^ c ^ d
            self.state[1][col] = a ^ gf_mul(0x02, b) ^ gf_mul(0x03, c) ^ d
            self.state[2][col] = a ^ b ^ gf_mul(0x02, c) ^ gf_mul(0x03, d)
            self.state[3][col] = gf_mul(0x03, a) ^ b ^ c ^ gf_mul(0x02, d)

    def inv_sub_state(self):
        """
        使用逆 S-Box 替换状态矩阵中的每个字节
        """
        for i in range(4):
            for j in range(self.Nb):
                self.state[i][j] = inv_sbox[self.state[i][j]]  # 使用逆 S-Box 替换当前字节

    def inv_shift_rows(self):
        """
        对状态矩阵的每一行进行逆循环移位
        """
        for row in range(1, 4):
            for _ in range(row):
                self.state[row] = np.roll(self.state[row], 1)  # 第 row 行右移 row 次

    def inv_mix_columns(self):
        """
        逆混合列操作，用于解密过程中的扩散
        """
        for col in range(self.Nb):
            a = self.state[0][col]
            b = self.state[1][col]
            c = self.state[2][col]
            d = self.state[3][col]

            self.state[0][col] = gf_mul(0x0E, a) ^ gf_mul(0x0B, b) ^ gf_mul(0x0D, c) ^ gf_mul(0x09, d)
            self.state[1][col] = gf_mul(0x09, a) ^ gf_mul(0x0E, b) ^ gf_mul(0x0B, c) ^ gf_mul(0x0D, d)
            self.state[2][col] = gf_mul(0x0D, a) ^ gf_mul(0x09, b) ^ gf_mul(0x0E, c) ^ gf_mul(0x0B, d)
            self.state[3][col] = gf_mul(0x0B, a) ^ gf_mul(0x0D, b) ^ gf_mul(0x09, c) ^ gf_mul(0x0E, d)


def aes_keyexpansion(key: np.array) -> np.array:
    """
    生成轮密钥

    :param key: 输入密钥，长度为 4 * Nk 的数组
    :return: 轮密钥数组 np数组
    """
    Nk = len(key) // 4  # 密钥长度（以字为单位）
    Nb = 4  # 状态矩阵的列数
    Nr = {4: 10, 6: 12, 8: 14}[Nk]  # 轮数
    w = np.zeros(Nb * (Nr + 1), dtype=np.uint32)

    for i in range(Nk):
        w[i] = ((np.uint32(key[4 * i]) << 24) | (np.uint32(key[4 * i + 1]) << 16) |
                (np.uint32(key[4 * i + 2]) << 8) | np.uint32(key[4 * i + 3]))

    for i in range(Nk, Nb * (Nr + 1)):
        temp = w[i - 1]
        if i % Nk == 0:
            temp = sub_word(rot_word(w[i - 1])) ^ rcon[i // Nk - 1]
        elif Nk > 6 and i % Nk == 4:
            temp = sub_word(temp)
        w[i] = w[i - Nk] ^ temp
    return w


def print_w(w: np.array):
    # 将 w 每个元素拆分成四个字节并竖向打印
    w_title = ""
    w_row0 = ""
    w_row1 = ""
    w_row2 = ""
    w_row3 = ""
    for i in range(len(w)):
        w_title += f"{i:02d} "
        w_row0 += f"{(w[i] >> 24) & 0xFF:02X} "
        w_row1 += f"{(w[i] >> 16) & 0xFF:02X} "
        w_row2 += f"{(w[i] >> 8) & 0xFF:02X} "
        w_row3 += f"{(w[i]) & 0xFF:02X} "
    # 将所有字节按列排列并打印在一行中
    print(w_title)
    print(w_row0)
    print(w_row1)
    print(w_row2)
    print(w_row3)


def aes_inv_keyexpansion(wi: np.array, w_round: int, wi_1: np.array, Nk: int) -> np.array:
    """
    Nk = 4 round 0 -> 10
    Nk = 6  round 0 -> 11
    Nk = 8  round 0 -> 13

    :param wi: w_round 轮的轮密钥
    :param w_round: 轮数
    :param wi_1: w_round + 1 轮的轮密钥
    :param Nk: aes128 4 aes192 6 aes256 8
    :return: 恢复出来的 key np数组
    """
    Nb = 4  # 状态矩阵的列数
    Nr = {4: 10, 6: 12, 8: 14}[Nk]  # 轮数
    w = np.zeros(Nb * (Nr + 1), dtype=np.uint32)
    key = np.zeros(Nk * Nb, dtype=np.uint8)

    for i in range(Nb):
        w[Nb * w_round + i] = wi[i]
        if Nk == 6 or Nk == 8:
            w[Nb * (w_round + 1) + i] = wi_1[i]

    w_startcol = w_round * 4 + (7 if Nk > 4 else 3)
    for i in range(w_startcol, Nk - 1, -1):
        if i % Nk == 0:
            w[i - Nk] = w[i] ^ sub_word(rot_word(w[i - 1])) ^ rcon[i // Nk - 1]
            pass
        elif i % 4 == 0 and Nk == 8:
            w[i - Nk] = w[i] ^ sub_word(w[i - 1])
            pass
        else:
            w[i - Nk] = w[i] ^ w[i - 1]

    for i in range(Nk):
        key[i * 4] = np.array(w[i] >> 24).astype(np.uint8)
        key[i * 4 + 1] = np.array(w[i] >> 16).astype(np.uint8)
        key[i * 4 + 2] = np.array(w[i] >> 8).astype(np.uint8)
        key[i * 4 + 3] = np.array(w[i]).astype(np.uint8)
    return key


